# List slicing
friends = ["Ram", "Shaam", "Rohan", "Sam", "Divya", 45]
#           0        1        2       3       4      5     +ve index 
#          -6       -5        -4     -3      -2     -1     -ve index 

print(friends[:3])  # start to 3
print(friends[0:4]) # 0 to 4 index
print(friends[-4:]) # -4 to end 
print(friends[-6:]) # -6 to end 
print(friends[2:5]) # 2 to 5
